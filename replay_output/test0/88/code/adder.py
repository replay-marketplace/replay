def add_two_numbers(a, b):
    """
    Adds two numbers and returns the result with an intentional bug.
    The bug adds 1 to the actual sum.
    
    Args:
        a: First number
        b: Second number
        
    Returns:
        The sum of a and b plus 1 (intentional bug)
    """
    return a + b + 1  # Intentional bug: adding 1 to the result
